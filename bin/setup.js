// ─── Deep auto-detect ───

async function detectProject() {
  const info = {
    name: "", description: "", stack: [], framework: "",
    hasTS: false, hasTailwind: false, hasI18n: false, hasAuth: false, hasDB: false,
    hasPayments: false, hasAI: false, deploy: "", dbName: "", authName: "", paymentName: "",
    components: [], apiRoutes: [], locales: [],
  };

  // package.json
  try {
    const pkg = JSON.parse(await readFile(join(process.cwd(), "package.json"), "utf8"));
    info.name = pkg.name || "";
    info.description = pkg.description || "";
    const allDeps = { ...pkg.dependencies, ...pkg.devDependencies };

    // Framework
    if (allDeps["next"]) { info.framework = "Next.js"; info.stack.push("Next.js"); }
    else if (allDeps["nuxt"]) { info.framework = "Nuxt"; info.stack.push("Nuxt"); }
    else if (allDeps["react"]) { info.framework = "React"; info.stack.push("React"); }
    else if (allDeps["vue"]) { info.framework = "Vue"; info.stack.push("Vue"); }
    else if (allDeps["svelte"] || allDeps["@sveltejs/kit"]) { info.framework = "SvelteKit"; info.stack.push("SvelteKit"); }
    else if (allDeps["express"]) { info.framework = "Express"; info.stack.push("Express"); }

    // Core
    if (allDeps["typescript"]) { info.hasTS = true; info.stack.push("TypeScript"); }
    if (allDeps["tailwindcss"]) { info.hasTailwind = true; info.stack.push("TailwindCSS"); }
    if (allDeps["framer-motion"]) info.stack.push("Framer Motion");

    // i18n
    if (allDeps["next-intl"] || allDeps["i18next"] || allDeps["react-intl"] || allDeps["next-i18next"]) {
      info.hasI18n = true; info.stack.push("i18n");
    }

    // DB
    if (allDeps["@supabase/supabase-js"]) { info.hasDB = true; info.dbName = "Supabase"; info.stack.push("Supabase"); }
    else if (allDeps["prisma"] || allDeps["@prisma/client"]) { info.hasDB = true; info.dbName = "Prisma"; info.stack.push("Prisma"); }
    else if (allDeps["drizzle-orm"]) { info.hasDB = true; info.dbName = "Drizzle"; info.stack.push("Drizzle"); }
    else if (allDeps["mongoose"]) { info.hasDB = true; info.dbName = "MongoDB"; info.stack.push("MongoDB"); }

    // Auth
    if (allDeps["next-auth"] || allDeps["@auth/core"]) { info.hasAuth = true; info.authName = "NextAuth"; info.stack.push("NextAuth"); }
    else if (allDeps["@supabase/auth-helpers-nextjs"] || allDeps["@supabase/ssr"]) { info.hasAuth = true; info.authName = "Supabase Auth"; info.stack.push("Supabase Auth"); }
    else if (allDeps["firebase"]) { info.hasAuth = true; info.authName = "Firebase Auth"; info.stack.push("Firebase"); }

    // Payments
    if (allDeps["stripe"]) { info.hasPayments = true; info.paymentName = "Stripe"; info.stack.push("Stripe"); }
    else if (allDeps["@paddle/paddle-js"]) { info.hasPayments = true; info.paymentName = "Paddle"; info.stack.push("Paddle"); }
    else if (allDeps["@tosspayments/payment-sdk"]) { info.hasPayments = true; info.paymentName = "Toss Payments"; info.stack.push("Toss Payments"); }

    // AI
    if (allDeps["openai"]) { info.hasAI = true; info.stack.push("OpenAI"); }
    else if (allDeps["@anthropic-ai/sdk"]) { info.hasAI = true; info.stack.push("Anthropic"); }
    else if (allDeps["@google/generative-ai"]) { info.hasAI = true; info.stack.push("Google AI"); }
  } catch {}

  // Detect deploy from vercel.json / netlify.toml / fly.toml
  if (await exists(join(process.cwd(), "vercel.json")) || await exists(join(process.cwd(), ".vercel"))) info.deploy = "Vercel";
  else if (await exists(join(process.cwd(), "netlify.toml"))) info.deploy = "Netlify";
  else if (await exists(join(process.cwd(), "fly.toml"))) info.deploy = "Fly.io";
  else if (await exists(join(process.cwd(), "Dockerfile"))) info.deploy = "Docker";

  // Scan components
  try {
    const compsDir = join(process.cwd(), "src", "components");
    if (await exists(compsDir)) {
      const files = await readdir(compsDir);
      info.components = files.filter(f => f.endsWith(".tsx") || f.endsWith(".vue") || f.endsWith(".svelte")).map(f => f.replace(/\.\w+$/, ""));
    }
  } catch {}

  // Scan API routes
  try {
    const apiDir = join(process.cwd(), "src", "app", "api");
    if (await exists(apiDir)) {
      const scanDir = async (dir, prefix = "") => {
        const entries = await readdir(dir, { withFileTypes: true });
        const routes = [];
        for (const e of entries) {
          if (e.isDirectory()) routes.push(...await scanDir(join(dir, e.name), `${prefix}/${e.name}`));
          else if (e.name === "route.ts" || e.name === "route.js") routes.push(prefix || "/");
        }
        return routes;
      };
      info.apiRoutes = await scanDir(apiDir);
    }
  } catch {}

  // Scan i18n locales
  try {
    for (const localeDir of ["src/i18n/dictionaries", "src/locales", "locales", "messages", "public/locales"]) {
      const dir = join(process.cwd(), localeDir);
      if (await exists(dir)) {
        const files = await readdir(dir);
        info.locales = files.filter(f => f.endsWith(".json")).map(f => f.replace(".json", ""));
        break;
      }
    }
  } catch {}

  return info;
}

// ─── Init: Zero-question harness generation ───

async function runInit(force) {
  log(`\n  ${BOLD}buildcrew init${RESET} v${VERSION}\n`);

  const harnessExists = await exists(join(HARNESS_DIR, "project.md"));

  if (harnessExists && !force) {
    log(`  ${YELLOW}Harness already exists at .claude/harness/${RESET}`);
    log(`  ${DIM}Use ${BOLD}--force${RESET}${DIM} to regenerate. Or just edit the files directly.${RESET}\n`);
    return;
  }

  // Backup existing harness before overwriting
  if (harnessExists && force) {
    const backupDir = join(process.cwd(), ".claude", "harness-backup");
    await mkdir(backupDir, { recursive: true });
    const harnessFiles = (await readdir(HARNESS_DIR)).filter(f => f.endsWith(".md"));
    for (const file of harnessFiles) {
      await copyFile(join(HARNESS_DIR, file), join(backupDir, file));
    }
    log(`  ${CYAN}Backed up ${harnessFiles.length} harness files → .claude/harness-backup/${RESET}\n`);
  }

  log(`  ${DIM}Scanning project...${RESET}\n`);
  const d = await detectProject();

  await mkdir(HARNESS_DIR, { recursive: true });

  // ─── project.md ───
  const projectMd = `# Project: ${d.name || "my-project"}

## Overview
${d.description || "<!-- Describe what this project does in 1-2 sentences -->"}

## Tech Stack
${d.stack.map(s => `- ${s}`).join("\n") || "<!-- Add your tech stack -->"}

## Framework
${d.framework || "<!-- Not detected -->"}

## Infrastructure
- **Deploy**: ${d.deploy || "<!-- Vercel / AWS / Netlify / etc. -->"}
- **Production URL**: <!-- https://your-app.com -->
- **TypeScript**: ${d.hasTS ? "Yes" : "No"}
- **CSS**: ${d.hasTailwind ? "TailwindCSS" : "<!-- Your CSS solution -->"}
- **i18n**: ${d.hasI18n ? `Yes (${d.locales.join(", ")})` : "No"}
- **Auth**: ${d.hasAuth ? d.authName : "No"}
- **Database**: ${d.hasDB ? d.dbName : "No"}
- **Payments**: ${d.hasPayments ? d.paymentName : "No"}
- **AI**: ${d.hasAI ? d.stack.find(s => ["OpenAI","Anthropic","Google AI"].includes(s)) || "Yes" : "No"}

## Key Components
${d.components.length > 0 ? d.components.map(c => `- ${c}`).join("\n") : "<!-- List your main components -->"}

## API Routes
${d.apiRoutes.length > 0 ? d.apiRoutes.map(r => `- \`/api${r}\``).join("\n") : "<!-- List your API endpoints -->"}

## Domain
- **Industry**: <!-- e.g., e-commerce, fintech, entertainment -->
- **User types**: <!-- e.g., free users, premium users, admins -->

## Key Domain Terms
<!-- Define project-specific terms so agents use consistent language -->
<!-- - term = definition -->

## Business Rules
<!-- Rules that affect feature development -->
<!-- - Free users: 3 actions per day -->
<!-- - Premium: unlimited access -->
`;

  // ─── rules.md ───
  const rulesMd = `# Team Rules

All agents read this before every task. Edit freely.

## Coding Conventions
${d.framework === "Next.js" ? `- Use App Router patterns
- Server components by default, "use client" only when needed
- Functional components only` : "<!-- Add your coding conventions -->"}
${d.hasTS ? "- Strict TypeScript — no \`any\` types" : ""}
${d.hasTailwind ? "- Use TailwindCSS utility classes, avoid custom CSS" : ""}
- No \`console.log\` in production code
- No default exports (except pages/layouts)

## Priorities
- UX over premature optimization
- Mobile-first responsive design
${d.hasI18n ? `- i18n: all user-facing text must be in locale files (${d.locales.join(", ")})` : ""}
${d.hasPayments ? "- Payment security: amounts validated server-side only" : ""}

## What to Avoid
- Class components
- \`any\` type assertions
- Inline styles (use Tailwind or CSS modules)
- Hardcoded strings in UI (use i18n)
- Direct DB access from client components

## Quality Standards
${d.hasTS ? "- \`npx tsc --noEmit\` must pass" : ""}
- \`npm run lint\` must pass
- \`npm run build\` must pass
${d.hasI18n && d.locales.length > 0 ? `- All ${d.locales.length} locale files must be in sync` : ""}
- No unused imports or variables

## Review Standards
${d.hasAuth ? "- All API routes must check authentication" : ""}
${d.hasPayments ? "- Payment mutations must be server-side and atomic" : ""}
${d.hasAI ? "- AI-generated content treated as untrusted (sanitize before render)" : ""}
- Check for XSS on any user input rendering
- Verify responsive layout at 375px, 768px, 1440px
`;

  await writeFile(join(HARNESS_DIR, "project.md"), projectMd);
  await writeFile(join(HARNESS_DIR, "rules.md"), rulesMd);

  // Auto-add relevant templates
  let templateCount = 0;
  const autoTemplates = [];
  if (d.hasDB) autoTemplates.push("erd");
  if (d.apiRoutes.length > 0) autoTemplates.push("api-spec");
  if (d.hasTailwind) autoTemplates.push("design-system");
  if (d.hasI18n || d.components.length > 5) autoTemplates.push("user-flow");
  autoTemplates.push("architecture");

  for (const name of autoTemplates) {
    const t = TEMPLATES[name];
    if (t && t.file) {
      await copyFile(join(TEMPLATES_SRC, t.file), join(HARNESS_DIR, `${name}.md`));
      templateCount++;
    }
  }

  // Print results
  log(`  ${GREEN}${BOLD}Harness generated!${RESET} (${2 + templateCount} files)\n`);
  log(`  ${GREEN} + ${RESET} project.md       ${DIM}auto-detected from package.json${RESET}`);
  log(`  ${GREEN} + ${RESET} rules.md         ${DIM}smart defaults for ${d.framework || "your stack"}${RESET}`);
  for (const name of autoTemplates) {
    log(`  ${GREEN} + ${RESET} ${(name + ".md").padEnd(17)}${DIM}${TEMPLATES[name].desc}${RESET}`);
  }

  // Show what was detected
  log(`\n  ${BOLD}Detected:${RESET}`);
  if (d.stack.length > 0) log(`  ${CYAN}Stack${RESET}       ${d.stack.join(", ")}`);
  if (d.deploy) log(`  ${CYAN}Deploy${RESET}      ${d.deploy}`);
  if (d.components.length > 0) log(`  ${CYAN}Components${RESET}  ${d.components.length} found`);
  if (d.apiRoutes.length > 0) log(`  ${CYAN}API Routes${RESET}  ${d.apiRoutes.length} found`);
  if (d.locales.length > 0) log(`  ${CYAN}Locales${RESET}     ${d.locales.join(", ")}`);

  // Interactive: offer remaining templates
  const remaining = Object.entries(TEMPLATES).filter(([name, t]) => t.file && !autoTemplates.includes(name));
  if (remaining.length > 0) {
    log(`\n  ${BOLD}Additional harness files available:${RESET}\n`);
    for (let i = 0; i < remaining.length; i++) {
      const [name, t] = remaining[i];
      log(`    ${BOLD}${i + 1})${RESET} ${CYAN}${name}${RESET} — ${DIM}${t.desc}${RESET}`);
    }
    log(`    ${BOLD}A)${RESET} All of the above`);
    log(`    ${BOLD}S)${RESET} Skip\n`);

    const answer = await ask(`  Which ones? ${DIM}(numbers separated by space, A for all, S to skip)${RESET} `);

    if (answer === "a" || answer === "all") {
      for (const [name, t] of remaining) {
        await copyFile(join(TEMPLATES_SRC, t.file), join(HARNESS_DIR, `${name}.md`));
        log(`  ${GREEN} + ${RESET} ${name}.md`);
      }
      log("");
    } else if (answer && answer !== "s" && answer !== "skip") {
      const nums = answer.split(/[\s,]+/).map(n => parseInt(n) - 1).filter(n => n >= 0 && n < remaining.length);
      for (const idx of nums) {
        const [name, t] = remaining[idx];
        await copyFile(join(TEMPLATES_SRC, t.file), join(HARNESS_DIR, `${name}.md`));
        log(`  ${GREEN} + ${RESET} ${name}.md`);
      }
      if (nums.length > 0) log("");
    }
  }

  log(`  ${BOLD}Next step:${RESET} Edit ${CYAN}.claude/harness/*.md${RESET} — fill in project-specific details.`);
  log(`  ${DIM}Look for <!-- comments --> — those are the parts to customize.${RESET}\n`);
}

// ─── Add: copy template ───

async function runAdd(type, force) {
  if (!type) {
    log(`\n  ${BOLD}Available harness templates:${RESET}\n`);
    const categories = { core: "Core (auto-created by init)", data: "Data", system: "System", design: "Design", domain: "Domain" };
    for (const [cat, label] of Object.entries(categories)) {
      const items = Object.entries(TEMPLATES).filter(([, v]) => v.category === cat);
      if (items.length === 0) continue;
      log(`  ${CYAN}${label}${RESET}`);
      for (const [name, info] of items) {
        const installed = await exists(join(HARNESS_DIR, `${name}.md`));
        const status = installed ? `${GREEN}installed${RESET}` : `${DIM}not installed${RESET}`;
        log(`    ${BOLD}${name.padEnd(16)}${RESET} ${status}  ${DIM}${info.desc}${RESET}`);
      }
      log("");
    }
    log(`  ${BOLD}Usage:${RESET} npx buildcrew add ${CYAN}<template>${RESET}\n`);
    return;
  }

  const template = TEMPLATES[type];
  if (!template) {
    log(`\n  ${RED}Unknown template: ${type}${RESET}`);
    log(`  ${DIM}Run ${BOLD}npx buildcrew add${RESET}${DIM} to see available templates.${RESET}\n`);
    return;
  }

  if (!template.file) {
    log(`\n  ${YELLOW}${type}.md is auto-generated by ${BOLD}npx buildcrew init${RESET}\n`);
    return;
  }

  const target = join(HARNESS_DIR, `${type}.md`);
  const targetExists = await exists(target);
  if (targetExists && !force) {
    log(`\n  ${YELLOW}${type}.md already exists.${RESET} Use ${BOLD}--force${RESET} to overwrite.\n`);
    return;
  }

  // Backup before overwriting
  if (targetExists && force) {
    const backupDir = join(process.cwd(), ".claude", "harness-backup");
    await mkdir(backupDir, { recursive: true });
    await copyFile(target, join(backupDir, `${type}.md`));
    log(`\n  ${CYAN}Backed up ${type}.md → .claude/harness-backup/${RESET}`);
  }

  await mkdir(HARNESS_DIR, { recursive: true });
  await copyFile(join(TEMPLATES_SRC, template.file), target);
  log(`\n  ${GREEN} + ${RESET} .claude/harness/${type}.md`);
  log(`  ${DIM}Edit to fill in your project details. Agents read it automatically.${RESET}\n`);
}

// ─── Harness status ───

async function runHarnessStatus() {
  log(`\n  ${BOLD}Harness${RESET} ${DIM}(.claude/harness/)${RESET}\n`);

  if (!(await exists(HARNESS_DIR))) {
    log(`  ${YELLOW}No harness found.${RESET} Run ${BOLD}npx buildcrew init${RESET}\n`);
    return;
  }

  const files = (await readdir(HARNESS_DIR)).filter(f => f.endsWith(".md")).sort();
  if (files.length === 0) { log(`  ${YELLOW}Empty.${RESET}\n`); return; }

  for (const file of files) {
    const name = file.replace(".md", "");
    const content = await readFile(join(HARNESS_DIR, file), "utf8");
    const lines = content.split("\n").length;
    const hasComments = content.includes("<!--");
    const status = hasComments ? `${YELLOW}needs editing${RESET}` : `${GREEN}configured${RESET}`;
    const t = TEMPLATES[name];
    const desc = t ? `${DIM}${t.desc}${RESET}` : `${DIM}(custom)${RESET}`;
    log(`  ${BOLD}${name.padEnd(16)}${RESET} ${status}  ${DIM}${lines} lines${RESET}  ${desc}`);
  }

  log(`\n  ${DIM}Edit files to replace <!-- comments --> with your content.${RESET}\n`);
}

// ─── Install agents ───

async function runInstall(force) {
  const files = (await readdir(AGENTS_SRC)).filter(f => f.endsWith(".md"));
  log(`\n  ${BOLD}buildcrew${RESET} v${VERSION}\n  ${DIM}15 AI agents for Claude Code${RESET}\n`);

  await mkdir(TARGET_DIR, { recursive: true });

  // Migrate: remove renamed agents from previous versions
  const deprecated = ["constitution.md"];
  for (const old of deprecated) {
    const oldPath = join(TARGET_DIR, old);
    if (await exists(oldPath)) {
      const { unlink } = await import("fs/promises");
      await unlink(oldPath);
      log(`  ${YELLOW} ✕ ${RESET} ${old} ${DIM}(renamed → buildcrew.md)${RESET}`);
    }
  }

  let installed = 0, skipped = 0, updated = 0;

  for (const file of files) {
    const target = join(TARGET_DIR, file);
    if (await exists(target)) {
      if (force) {
        await copyFile(join(AGENTS_SRC, file), target);
        log(`  ${GREEN} + ${RESET} ${file} ${DIM}(overwritten)${RESET}`);
        installed++;
      } else {
        // Check version: compare installed vs source
        const installedContent = await readFile(target, "utf8");
        const sourceContent = await readFile(join(AGENTS_SRC, file), "utf8");
        const installedVer = (installedContent.match(/^version:\s*(.+)$/m) || [])[1];
        const sourceVer = (sourceContent.match(/^version:\s*(.+)$/m) || [])[1];
        if (sourceVer && installedVer && sourceVer !== installedVer) {
          await copyFile(join(AGENTS_SRC, file), target);
          log(`  ${CYAN} ↑ ${RESET} ${file} ${DIM}(${installedVer} → ${sourceVer})${RESET}`);
          updated++;
        } else {
          skipped++;
        }
      }
    } else {
      await copyFile(join(AGENTS_SRC, file), target);
      log(`  ${GREEN} + ${RESET} ${file}`);
      installed++;
    }
  }

  log("");
  const parts = [];
  if (installed > 0) parts.push(`${installed} installed`);
  if (updated > 0) parts.push(`${updated} updated`);
  if (skipped > 0) parts.push(`${skipped} up-to-date`);
  if (installed > 0 || updated > 0) log(`  ${GREEN}${BOLD}Done!${RESET} ${parts.join(", ")}.\n`);
  else log(`  ${GREEN}All agents up-to-date.${RESET} (v${VERSION})\n`);

  // ─── Step 2: Playwright MCP ───
  try {
    const { execSync } = await import("child_process");
    const mcpList = execSync("claude mcp list 2>/dev/null", { encoding: "utf8" });
    if (!mcpList.includes("playwright")) {
      log(`  ${YELLOW}Playwright MCP${RESET} is needed for browser testing agents.`);
      log(`  ${DIM}Used by: browser-qa, design-reviewer, canary-monitor, designer${RESET}\n`);
      const answer = await ask(`  Install Playwright MCP now? ${BOLD}(Y/n)${RESET} `);
      if (answer === "" || answer === "y" || answer === "yes") {
        log(`\n  ${DIM}Running: claude mcp add playwright -- npx @anthropic-ai/mcp-server-playwright${RESET}\n`);
        try {
          execSync("claude mcp add playwright -- npx @anthropic-ai/mcp-server-playwright", { stdio: "inherit" });
          log(`\n  ${GREEN}${BOLD}Playwright MCP installed!${RESET}\n`);
        } catch {
          log(`\n  ${RED}Failed to install.${RESET} Run manually:`);
          log(`  ${BOLD}claude mcp add playwright -- npx @anthropic-ai/mcp-server-playwright${RESET}\n`);
        }
      } else {
        log(`\n  ${DIM}Skipped. Run later: claude mcp add playwright -- npx @anthropic-ai/mcp-server-playwright${RESET}\n`);
      }
    } else {
      log(`  ${GREEN}Playwright MCP:${RESET} installed ✓\n`);
    }
  } catch { /* claude CLI not available, skip */ }

  // ─── Step 2b: Framer Motion ───
  try {
    const pkg = JSON.parse(await readFile(join(process.cwd(), "package.json"), "utf8"));
    const allDeps = { ...pkg.dependencies, ...pkg.devDependencies };
    if (!allDeps["framer-motion"]) {
      log(`  ${YELLOW}Framer Motion${RESET} is needed for designer agent animations.`);
      log(`  ${DIM}Used by: designer (entrance, scroll, hover, page transitions)${RESET}\n`);
      const answer = await ask(`  Install Framer Motion now? ${BOLD}(Y/n)${RESET} `);
      if (answer === "" || answer === "y" || answer === "yes") {
        const { execSync } = await import("child_process");
        log(`\n  ${DIM}Running: npm install framer-motion${RESET}\n`);
        try {
          execSync("npm install framer-motion", { stdio: "inherit", cwd: process.cwd() });
          log(`\n  ${GREEN}${BOLD}Framer Motion installed!${RESET}\n`);
        } catch {
          log(`\n  ${RED}Failed to install.${RESET} Run manually: ${BOLD}npm install framer-motion${RESET}\n`);
        }
      } else {
        log(`\n  ${DIM}Skipped. Run later: npm install framer-motion${RESET}\n`);
      }
    } else {
      log(`  ${GREEN}Framer Motion:${RESET} installed ✓\n`);
    }
  } catch { /* no package.json, skip */ }

  // ─── Step 2c: GitHub CLI ───
  try {
    const { execSync } = await import("child_process");
    execSync("which gh 2>/dev/null", { encoding: "utf8" });
    log(`  ${GREEN}GitHub CLI:${RESET} installed ✓\n`);
  } catch {
    log(`  ${YELLOW}GitHub CLI (gh)${RESET} is needed for shipper agent PR creation.`);
    log(`  ${DIM}Used by: shipper (gh pr create, auto push + PR)${RESET}\n`);
    const answer = await ask(`  Install GitHub CLI now? ${BOLD}(Y/n)${RESET} `);
    if (answer === "" || answer === "y" || answer === "yes") {
      try {
        const { execSync } = await import("child_process");
        const platform = process.platform;
        if (platform === "darwin") {
          log(`\n  ${DIM}Running: brew install gh${RESET}\n`);
          execSync("brew install gh", { stdio: "inherit" });
        } else if (platform === "linux") {
          log(`\n  ${DIM}Running: sudo apt install gh${RESET}\n`);
          execSync("sudo apt install gh -y", { stdio: "inherit" });
        } else {
          log(`\n  ${DIM}Visit: https://cli.github.com/${RESET}\n`);
        }
        log(`\n  ${GREEN}${BOLD}GitHub CLI installed!${RESET} Run ${BOLD}gh auth login${RESET} to authenticate.\n`);
      } catch {
        log(`\n  ${RED}Failed to install.${RESET} Visit: ${BOLD}https://cli.github.com/${RESET}\n`);
      }
    } else {
      log(`\n  ${DIM}Skipped. Without gh, shipper will output PR description for manual creation.${RESET}\n`);
    }
  }

  // ─── Step 2d: CC hooks (agent lifecycle banners + events.jsonl) ───
  try {
    const hooksInstalled = await areHooksInstalled();
    if (hooksInstalled) {
      log(`  ${GREEN}Hooks:${RESET} installed ✓\n`);
    } else {
      log(`  ${YELLOW}Hooks${RESET} print a colored banner to your terminal whenever an`);
      log(`  agent starts/completes or a file is written. Also feeds ${BOLD}npx buildcrew watch${RESET}.`);
      log(`  ${DIM}Writes .claude/settings.json (+ recommended permissions)${RESET}\n`);
      const answer = await ask(`  Install hooks? ${BOLD}(Y/n)${RESET} `);
      if (answer === "" || answer === "y" || answer === "yes") {
        try {
          const { install } = await import("../lib/install-hooks.js");
          const result = await install({
            scope: "project",
            cwd: process.cwd(),
            withPermissions: true,
          });
          log(`\n  ${GREEN}${BOLD}Hooks installed!${RESET}`);
          log(`  ${DIM}  hooks:       ${result.settingsPath}${RESET}`);
          if (result.permissions?.permPath) {
            log(`  ${DIM}  permissions: ${result.permissions.permPath}${RESET}`);
          }
          log(`\n  ${BOLD}Live monitor:${RESET} ${CYAN}npx buildcrew watch${RESET} ${DIM}(in a separate pane)${RESET}\n`);
        } catch (err) {
          log(`\n  ${RED}Hook install failed:${RESET} ${err.message}\n`);
        }
      } else {
        log(`\n  ${DIM}Skipped. Hooks not installed.${RESET}\n`);
      }
    }
  } catch { /* ignore, non-fatal */ }

  // ─── Step 3: Project harness ───
  if (!(await exists(join(HARNESS_DIR, "project.md")))) {
    const initAnswer = await ask(`  Generate project harness? ${DIM}(auto-detects your stack)${RESET} ${BOLD}(Y/n)${RESET} `);
    if (initAnswer === "" || initAnswer === "y" || initAnswer === "yes") {
      log("");
      await runInit(false);
    } else {
      log(`\n  ${DIM}Skipped. Run later: npx buildcrew init${RESET}\n`);
    }
  } else {
    log(`  ${GREEN}Project harness:${RESET} exists ✓\n`);
  }

  log(`  ${BOLD}Start:${RESET}  @buildcrew [your request]\n`);
}

// ─── List / Uninstall ───

async function runList() {
  const files = (await readdir(AGENTS_SRC)).filter(f => f.endsWith(".md"));
  log(`\n  ${BOLD}buildcrew${RESET} v${VERSION} — ${files.length} agents\n`);
  for (const file of files) {
    const content = await readFile(join(AGENTS_SRC, file), "utf8");
    const name = (content.match(/^name:\s*(.+)$/m) || [])[1] || file.replace(".md", "");
    const desc = (content.match(/^description:\s*(.+)$/m) || [])[1] || "";
    const model = (content.match(/^model:\s*(.+)$/m) || [])[1] || "sonnet";
    const modelTag = model === "opus" ? `${MAGENTA}opus${RESET}` : `${DIM}sonnet${RESET}`;
    log(`  ${name === "buildcrew" ? BOLD : ""}${name.padEnd(20)}${RESET} ${modelTag}  ${DIM}${desc.slice(0, 55)}${RESET}`);
  }
  log("");
}

async function areHooksInstalled() {
  try {
    const settingsPath = join(process.cwd(), ".claude", "settings.json");
    const content = await readFile(settingsPath, "utf-8");
    return content.includes("buildcrew-hook");
  } catch {
    return false;
  }
}

async function runWatch() {
  // Pass through to bin/watch.js — the terminal-native live monitor.
  const watchEntry = resolve(__dirname, "watch.js");
  const passthrough = process.argv.slice(3);
  const { spawn } = await import("child_process");
  const child = spawn(process.execPath, [watchEntry, ...passthrough], {
    stdio: "inherit",
    cwd: process.cwd(),
    env: process.env,
  });
  child.on("exit", (code) => process.exit(code ?? 0));
  child.on("error", (err) => {
    console.error(`${RED}Watch failed to start:${RESET} ${err.message}`);
    process.exit(1);
  });
}

async function runReport() {
  // Show coherence-report.md output by the coherence-auditor agent.
  // Usage:
  //   npx buildcrew report                Show latest coherence-report
  //   npx buildcrew report --list         List all reports with timestamps
  //   npx buildcrew report <feature>      Show specific feature's report
  //   npx buildcrew report --raw          Print raw markdown (for piping)
  const args = process.argv.slice(3);
  const wantList = args.includes("--list") || args.includes("-l");
  const wantRaw = args.includes("--raw");
  const featureArg = args.find(a => !a.startsWith("-"));

  const PIPELINE_DIR = join(process.cwd(), ".claude", "pipeline");
  if (!(await exists(PIPELINE_DIR))) {
    log(`${YELLOW}No pipeline runs found yet.${RESET}`);
    log(`${DIM}Run ${BOLD}@buildcrew <feature>${RESET}${DIM} in Claude Code to generate one.${RESET}\n`);
    return;
  }

  // Collect all coherence-report.md files and their mtimes
  const features = await readdir(PIPELINE_DIR, { withFileTypes: true });
  const reports = [];
  const { stat } = await import("fs/promises");
  for (const entry of features) {
    if (!entry.isDirectory()) continue;
    const reportPath = join(PIPELINE_DIR, entry.name, "coherence-report.md");
    if (await exists(reportPath)) {
      const s = await stat(reportPath);
      reports.push({ feature: entry.name, path: reportPath, mtime: s.mtime });
    }
  }

  if (reports.length === 0) {
    log(`${YELLOW}No coherence-report.md found in any pipeline run.${RESET}`);
    log(`${DIM}coherence-auditor runs at the end of Feature mode. If you ran a feature recently and don't see a report, check your buildcrew version (need >= 1.9.0).${RESET}\n`);
    return;
  }

  reports.sort((a, b) => b.mtime - a.mtime);

  if (wantList) {
    log(`\n  ${BOLD}Coherence reports${RESET} ${DIM}(newest first)${RESET}\n`);
    for (const r of reports) {
      const ago = ((Date.now() - r.mtime) / 1000 / 60) | 0;
      const when = ago < 60 ? `${ago}m ago` : ago < 1440 ? `${(ago/60)|0}h ago` : `${(ago/1440)|0}d ago`;
      log(`  ${CYAN}${r.feature.padEnd(30)}${RESET} ${DIM}${when.padStart(8)}${RESET}  ${DIM}${r.path}${RESET}`);
    }
    log(`\n  ${DIM}Show one: ${BOLD}npx buildcrew report ${CYAN}<feature>${RESET}\n`);
    return;
  }

  // Pick target report
  let target;
  if (featureArg) {
    target = reports.find(r => r.feature === featureArg);
    if (!target) {
      log(`${RED}No coherence-report for feature "${featureArg}".${RESET}`);
      log(`${DIM}List all: ${BOLD}npx buildcrew report --list${RESET}\n`);
      process.exit(1);
    }
  } else {
    target = reports[0]; // latest
  }

  const content = await readFile(target.path, "utf8");

  if (wantRaw) {
    process.stdout.write(content);
    return;
  }

  // Pretty header + content. If TTY supports it, try less for paging.
  const header = `${BOLD}${CYAN}═══ ${target.feature} ═══${RESET}  ${DIM}${target.path}${RESET}\n\n`;

  if (process.stdout.isTTY && content.split("\n").length > process.stdout.rows) {
    // Try paging through `less -R` (preserves ANSI). Fallback to direct print.
    try {
      const { spawn } = await import("child_process");
      const less = spawn("less", ["-R", "-X"], { stdio: ["pipe", "inherit", "inherit"] });
      less.stdin.write(header + content);
      less.stdin.end();
      await new Promise((resolve) => less.on("exit", resolve));
      return;
    } catch {
      // fall through to direct print
    }
  }

  process.stdout.write(header + content + "\n");
}

async function runUninstall() {
  const files = (await readdir(AGENTS_SRC)).filter(f => f.endsWith(".md"));
  if (!(await exists(TARGET_DIR))) { log(`${YELLOW}No agents found.${RESET}`); return; }
  let removed = 0;
  for (const file of files) {
    const target = join(TARGET_DIR, file);
    if (await exists(target)) { const { unlink } = await import("fs/promises"); await unlink(target); removed++; }
  }
  log(`${GREEN}Removed ${removed} agent files.${RESET}`);
}

// ─── Main ───

async function main() {
  const args = process.argv.slice(2);
  const command = args[0];
  const subcommand = args[1];
  const force = args.includes("--force") || args.includes("-f");

  if (args.includes("--version") || args.includes("-v")) { log(`buildcrew v${VERSION}`); return; }

  if (args.includes("--help") || args.includes("-h")) {
    log(`
  ${BOLD}buildcrew${RESET} v${VERSION} — 15 AI agents for Claude Code

  ${BOLD}Commands:${RESET}
    npx buildcrew              Install agents (also offers hooks + harness)
    npx buildcrew init         Auto-generate project harness (zero questions)
    npx buildcrew add          List harness templates
    npx buildcrew add <name>   Add a harness template
    npx buildcrew harness      Show harness file status
    npx buildcrew watch        Live terminal monitor (stays in your shell)
    npx buildcrew report       Show latest coherence-report (team coordination score)
    npx buildcrew report --list   List all coherence reports

  ${BOLD}Options:${RESET}
    --force, -f    Overwrite existing files
    --list, -l     List all agents
    --uninstall    Remove agents
    --version      Show version

  ${BOLD}Setup:${RESET}
    ${GREEN}1.${RESET} npx buildcrew           ${DIM}Install agents + optional hooks${RESET}
    ${GREEN}2.${RESET} npx buildcrew init      ${DIM}Auto-generate harness from codebase${RESET}
    ${GREEN}3.${RESET} Edit .claude/harness/   ${DIM}Customize (replace <!-- comments -->)${RESET}
    ${GREEN}4.${RESET} npx buildcrew watch     ${DIM}(optional) live terminal monitor${RESET}
    ${GREEN}5.${RESET} @buildcrew [task]       ${DIM}Start working${RESET}

  ${BOLD}More info:${RESET} https://github.com/z1nun/buildcrew
`);
    return;
  }

  // Subcommand routing takes priority over global --list (so `report --list` works)
  if (command === "init") return runInit(force);
  if (command === "add") return runAdd(subcommand, force);
  if (command === "harness") return runHarnessStatus();
  if (command === "watch") return runWatch();
  if (command === "report") return runReport();
  if (args.includes("--list") || args.includes("-l")) return runList();
  if (args.includes("--uninstall")) return runUninstall();

  return runInstall(force);
}

